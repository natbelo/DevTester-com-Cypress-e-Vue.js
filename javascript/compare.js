// igual (==)
//Retorna verdadeiro se os valores comparados forem iguais

var valor1 = 10
var valor2 = 20

console.log(valor1 == valor2)
valor2 = 10
console.log(valor1 == valor2)

valor2 = "10"
console.log(valor1 == valor2)

//igual (mais crítico ===)
//retorna verdadeiro se os valores e tipos forem iguais
console.log(valor1 === valor2)

//não igual (!=)
//retorna verdadeiro se os valores comparados não forem iguais
valor1 = 10
valor2 = 5
console.log(valor1 != valor2)